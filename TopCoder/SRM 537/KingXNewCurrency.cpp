class KingXNewCurrency {
    public:
    int howMany(int A, int B, int X) {
      if(A % X == 0 && B % X == 0) return -1;

      int ans = 0;

      for(int i = 1; i <= 200; i++) {
        bool a = false, b = false;

        for(int p = 0; p <= 200; p++) {
          for(int q = 0; q <= 200; q++) {
            if(p * X + q * i == A) a = true;
            if(p * X + q * i == B) b = true;
          }
        }
        if(a && b) ans++;  
      }

      return ans;
    }
};

