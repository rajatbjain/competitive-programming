class TextStatistics {
    public:
    double averageLength(string text) {
        int len = 0, words = 0;
        int cnt = 0;
        for(int i = 0; i < text.size(); i++) {
            if((text[i] >= 'a' && text[i] <= 'z') || (text[i] >= 'A' && text[i] <= 'Z') ){
                cnt++;
            }
            else {
                if(cnt == 0) continue;
                else {
                    len += cnt;
                    cnt = 0;
                    words++;
                }
            }
        }
        if(cnt) {
            len += cnt;
            words++;
        }
        if(words == 0) return 0.00;
        return (double)len / words;
    }
};

